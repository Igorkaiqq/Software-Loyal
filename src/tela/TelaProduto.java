        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.jtProdutoTabela.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.jtProdutoTabela.setRowSorter(classificador);
