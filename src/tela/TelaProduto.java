    private void salvarProduto(){
        modelProduto.setProduto_nome(this.jtfProdutoNome.getText()); 
        modelProduto.setProduto_marca(this.jtfProdutoMarca.getText()); 
        modelProduto.setProduto_cor(this.jtfProdutoCor.getText()); 
        modelProduto.setProduto_tamanho((String) this.jcProdutoTamanho.getSelectedItem());
        modelProduto.setProduto_estoque(Integer.parseInt(this.jtfProdutoEstoque.getText()));
        modelProduto.setProduto_valor(Double.parseDouble(this.jtfProdutoValor.getText().replace(',', '.')));
        if (ControllerProduto.salvarProdutoController(modelProduto)>0) {
            JOptionPane.showMessageDialog(this, " O produto foi registrado com sucesso.");
            carregarProdutos();
            limparcampos();
            habilitarCampos(false);
            jbProdutoSalvar.setEnabled(false);
            jbProdutoAlterar.setEnabled(true);
            jbProdutoExcluir.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this, " Erro ao registrar o produto.");
        }
    }
    
    private void alterarProduto(){
        modelProduto.setProduto_nome(this.jtfProdutoNome.getText()); 
        modelProduto.setProduto_marca(this.jtfProdutoMarca.getText()); 
        modelProduto.setProduto_cor(this.jtfProdutoCor.getText()); 
        modelProduto.setProduto_tamanho((String) this.jcProdutoTamanho.getSelectedItem());
        modelProduto.setProduto_estoque(Integer.parseInt(this.jtfProdutoEstoque.getText()));
        modelProduto.setProduto_valor(Double.parseDouble(this.jtfProdutoValor.getText()));
        if (ControllerProduto.atualizarProdutoController(modelProduto)) {
            JOptionPane.showMessageDialog(this, " O produto foi alterado com sucesso.");
            carregarProdutos();
            limparcampos();
            habilitarCampos(false);
            
        }else{
            JOptionPane.showMessageDialog(this, " Erro ao alterar o produto.");
        }
    }
    
