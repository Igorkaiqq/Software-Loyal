    private void jbProdutoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProdutoSalvarActionPerformed
        // TODO add your handling code here:
        jtfProdutoCodigo.setText("");
        if (salvarAlteracao=="salvar") {
            this.salvarProduto();
        }else if(salvarAlteracao=="alterar") {
            this.alterarProduto();
        }

    }//GEN-LAST:event_jbProdutoSalvarActionPerformed

    private void jbProdutoNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProdutoNovoActionPerformed
        // TODO add your handling code here:
        habilitarCampos(true);
        limparcampos();
        salvarAlteracao = "salvar";
        jbProdutoSalvar.setEnabled(true);
        jbProdutoAlterar.setEnabled(false);
        jbProdutoExcluir.setEnabled(false);
    }//GEN-LAST:event_jbProdutoNovoActionPerformed

    private void jbProdutoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProdutoAlterarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = this.jtProdutoTabela.getSelectedRow();
        salvarAlteracao = "alterar";
        habilitarCampos(true);
        try {
            int codigoProduto = (int) this.jtProdutoTabela.getValueAt(linhaSelecionada, 0);
            modelProduto = ControllerProduto.getProdutoController(codigoProduto);
            this.jtfProdutoCodigo.setText(String.valueOf(modelProduto.getId_produto()));
            this.jtfProdutoNome.setText(modelProduto.getProduto_nome());
            this.jtfProdutoMarca.setText(modelProduto.getProduto_marca());
            this.jtfProdutoCor.setText(modelProduto.getProduto_cor());
            this.jtfProdutoEstoque.setText(String.valueOf(modelProduto.getProduto_estoque()));
            this.jtfProdutoValor.setText(String.valueOf(modelProduto.getProduto_valor()));
            this.jcProdutoTamanho.setSelectedItem(modelProduto.getProduto_tamanho());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, " Erro ao alterar o produto.");
        }
    }//GEN-LAST:event_jbProdutoAlterarActionPerformed

    private void jbProdutoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProdutoExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = jtProdutoTabela.getSelectedRow();
        int codigoProduto =(int) jtProdutoTabela.getValueAt(linhaSelecionada, 0);
        if (ControllerProduto.excluirProdutoController(codigoProduto)) {
            JOptionPane.showMessageDialog(this, " O produto foi excluido com sucesso.");
            carregarProdutos();
        }else{
            JOptionPane.showMessageDialog(this, " Erro ao excluir o produto.");
        }

    }//GEN-LAST:event_jbProdutoExcluirActionPerformed

