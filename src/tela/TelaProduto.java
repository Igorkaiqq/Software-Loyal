    private void carregarProdutos(){
        listaModelProduto = ControllerProduto.getListaProdutoController();
        DefaultTableModel model = (DefaultTableModel) jtProdutoTabela.getModel();
        model.setNumRows(0); 
        int contador = listaModelProduto.size();
        for (int i = 0; i < contador; i++) {
            model.addRow(new Object[]{
                listaModelProduto.get(i).getId_produto(),
                listaModelProduto.get(i).getProduto_nome(),  
                listaModelProduto.get(i).getProduto_marca(),  
                listaModelProduto.get(i).getProduto_cor(),  
                listaModelProduto.get(i).getProduto_estoque(),
                listaModelProduto.get(i).getProduto_tamanho(),
                listaModelProduto.get(i).getProduto_valor()
            });
            
        }
        
    }
    
