    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = jtVendasFeitas.getSelectedRow();
        int codigo = (int) jtVendasFeitas.getValueAt(linhaSelecionada, 0);
        listaModelProdutos = new ArrayList<>();
        listaModelProdVendProd = controllerProdVendProd.getListaProdVendProdController(codigo);
        for (int i = 0; i < listaModelProdVendProd.size(); i++) {
            modelProduto = new ModelProduto();
            modelProduto.setId_produto(listaModelProdVendProd.get(i).getModelProdutos().getId_produto());
            //volta com os produtos para o estoque
            modelProduto.setProduto_estoque(listaModelProdVendProd.get(i).getModelProdutos().getProduto_estoque()
            + listaModelProdVendProd.get(i).getModelVendasProdutos().getVendasprod_qnt()
            );
            listaModelProdutos.add(modelProduto);
        }
        if (controllerProduto.atualizarEstoqueProdutoController(listaModelProdutos)) {
            controllerVendasProdutos.excluirVendasProdutosController(codigo);
            if (controllerVendas.excluirVendasController(codigo)) {
            JOptionPane.showMessageDialog(this, "Venda excluida com sucesso", "Atenção", JOptionPane.WARNING_MESSAGE);
            carregarVendas();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir a venda", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir a venda", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed
 
