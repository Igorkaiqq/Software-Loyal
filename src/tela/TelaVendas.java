    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.jtVendasFeitas.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        
        this.jtVendasFeitas.setRowSorter(classificador);
        String textoPesquisado = jtfPesquisa.getText();
        if (textoPesquisado.equals(textoPesquisado)) {
            classificador.setRowFilter(RowFilter.regexFilter(textoPesquisado, 1));
        } else {
            classificador.setRowFilter(RowFilter.regexFilter(textoPesquisado.toLowerCase(), 1));
        }
        
    }//GEN-LAST:event_jbPesquisarActionPerformed

