    private void desconto(){ 
        try {
            DecimalFormat valorComDesconto = new DecimalFormat("0.00");
        double valor = Double.parseDouble(jtfValorTotal.getText())*(1-(Double.parseDouble(jtfDesconto.getText())/100));
        valorComDesconto.setRoundingMode(RoundingMode.HALF_UP);
        
        jtfValorTotal.setText(String.valueOf(valorComDesconto.format(valor).replace(',', '.')));
        } catch (NumberFormatException e) {
        }
      
        
    }
    
