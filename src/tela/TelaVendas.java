    public int localizarMaiorCodigoVenda() {
        listaModelVendasClientes = controllerVendasCliente.getListaVendasClienteController();
        int codigo = 0;
        for (int i = 0; i < listaModelVendasProdutos.size(); i++) { 
            
            if (listaModelVendasClientes.get(i).getModelVendas().getId_vendas()>codigo) {
                codigo = listaModelVendasClientes.get(i).getModelVendas().getId_vendas();
            }
    
        }
       codigo = codigo+1;
       return codigo;
    }
    
    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:
        modelVendas.setCliente(Integer.parseInt(jtfCodigoCliente.getText()));
        listaModelVendasProdutos = new ArrayList<>();
        //Pega a data atual para salvar na venda
        try {
            modelVendas.setVenda_data(datas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {

        }

        double soma=0, valor=0;
        //soma o valor da compra
        int cont = jtVendas.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) jtVendas.getValueAt(i, 4);
            soma = soma + valor;
        }

        int codigoVenda = 0, codigoProduto=0, qntdProdutoVenda=0, qntdEstoque = 0;
        

        
        //seta no banco de dados os valores respectivos da compra
        modelVendas.setVenda_valorliquido(Double.parseDouble(jtfValorTotal.getText().replace(',', '.')));
        modelVendas.setVenda_valorbruto(soma);
        modelVendas.setVenda_desconto(Double.parseDouble(jtfDesconto.getText()));
        codigoVenda = localizarMaiorCodigoVenda();
        
        int contaLinhas = jtVendas.getRowCount();
        
        for (int i = 0; i < contaLinhas; i++) {
            //salvar produtos da venda
            modelVendasProdutos = new ModelVendasProdutos();
            modelProduto = new ModelProduto();
            modelVendasProdutos.setProduto((int) jtVendas.getValueAt(i, 0));
            modelVendasProdutos.setVendas(codigoVenda);
            modelVendasProdutos.setVendasprod_valor((double) jtVendas.getValueAt(i, 3));
            modelVendasProdutos.setVendasprod_qnt(Integer.parseInt(jtVendas.getValueAt(i, 2).toString()));
            //dar baixa no estoque
            qntdEstoque = (controllerProduto.retornarProdutoController(codigoProduto).getProduto_estoque());
            qntdProdutoVenda = Integer.parseInt(jtVendas.getValueAt(i, 2).toString());
            if (qntdEstoque>qntdProdutoVenda) { //verifica se tem a quantidade de estoque necessária para a venda
            
            } else {
            codigoProduto=(int) jtVendas.getValueAt(i, 0);
            modelProduto.setId_produto(codigoProduto);
            modelProduto.setProduto_estoque(controllerProduto.retornarProdutoController(codigoProduto).getProduto_estoque()-qntdProdutoVenda);
            listaModelVendasProdutos.add(modelVendasProdutos);
            listaModelProdutos.add(modelProduto);

            if (controllerVendas.salvarVendasController(modelVendas)>0) {
                JOptionPane.showMessageDialog(this, "Venda concluída com sucesso");

            } else {
                JOptionPane.showMessageDialog(this, "Erro ao finalizar venda", "ERRO", JOptionPane.ERROR_MESSAGE);
             } 
            }
            
        }
        
        // Salva os produtos na venda
        if(controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)){
            controllerProduto.atualizarEstoqueProdutoController(listaModelProdutos); 
           // JOptionPane.showMessageDialog(this, "Os produtos foram salvos com sucesso");
            limparCampos();
            carregarVendas();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar os produtos", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jbSalvarActionPerformed
    private void carregarVendas(){
        DefaultTableModel modelo = (DefaultTableModel) jtVendasFeitas.getModel();
        modelo.getDataVector().removeAllElements();
        listaModelVendasClientes = controllerVendasCliente.getListaVendasClienteController();
        int cont = listaModelVendasClientes.size();
       modelo.setNumRows(0);
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[] {
            listaModelVendasClientes.get(i).getModelVendas().getId_vendas(),
            listaModelVendasClientes.get(i).getModelCliente().getCliente_nome(),
            listaModelVendasClientes.get(i).getModelVendas().getVenda_data(),
            listaModelVendasClientes.get(i).getModelVendas().getVenda_valorliquido()
            });
        }
   }
